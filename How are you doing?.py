# This script interacts with the user and responds based on their input.

def main():
    print("Hello! How are you doing today?")
    response = input("> ").strip().lower()  # Get user input and handle cases

    if response == "bad":
        print("I'm sorry to hear that. I hope your day gets better!")
    else:
        print(f"That's great to hear: {response}" if response else "I hope your day gets better!")

if __name__ == "__main__":
    main()
